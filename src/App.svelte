	<p>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Enough with the
		questions, let’s talk about how and why you might want to become a game
		warden. As said before, they do tons of different things, and you can’t
		just be anyone looking to become a Game Warden, there’s some pretty
		strict requirements. First off, you need to be at least 21 years old
		because you’ll be dealing with firearms and similar things, you’re
		basically a police officer of nature, so you never know what you’re
		going to come across. You also have to be physically fit, you’ll be
		running, jumping, climbing and navigating rough terrain, sometimes, even
		chasing down people in different environments. Game Wardens are expected
		to know everything about wildlife regulations to make sure people are
		within the legal limits. You’re going to have to get a boating license,
		practice water rescues, learn defensive tactics, drive many kinds of
		4-wheel offroad vehicles, first aid, law enforcement, firearm training,
		and again, you must be physically fit.
	</p>
