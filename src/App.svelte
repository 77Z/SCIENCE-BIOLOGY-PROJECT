	<p>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;You’ve got questions,
		and I’m here to answer ‘em! First off, what’s a Game Warden? Game
		Wardens, also known as Conservation Wardens, have a vast and wide
		variety of skills that help them with their duties. Ok, so what does a
		Game Warden do? They spend most of their time on field almost acting
		like a police officer of nature. Sometimes you will hear them as Game
		and Fish Wardens, because they regulate fish too, and if you didn’t
		know, game, refers to hunting because they regulate that too. I’ve heard
		some sources refer to Game Wardens as detectives because they have to
		hunt down and capture poachers, and depending on what kind of poaching
		is happening, it can be really hard to determine if someone is poaching,
		or if they’re genuinely hunting. Because you can’t check everything.
		Again, it might be easier to find people hunting with laser sights(which
		is not allowed) than it might be to find people with 100% valid hunting
		or fishing licences, or if they’re using a legal firearm.
	</p>
